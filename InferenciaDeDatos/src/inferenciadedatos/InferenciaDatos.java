    public boolean esBooleano(String dato){
        if(dato.equals("false")|| dato.equals("true")){
            return true;
        }
        return false;
    }
    public boolean esChar(String dato){
        if(dato.length()==1&& dato.charAt(0)!='Ã±'){
            return true;
        }
        return false;
    }
    public boolean esByte(String dato){
        boolean isByte =  dato.matches("[+-]?\\d*?");
        if(isByte){ // ya tenemos numeros, validar rango
            int numero=Integer.parseInt(dato);
            if(numero>=-127&&numero<=128){
                return true;
            }
        }
        return false;
    }
    public boolean esShort(String dato){
        boolean isShort =  dato.matches("[+-]?\\d*?");
        if(isShort){ // ya tenemos numeros, validar rango
            int numero=Integer.parseInt(dato);
            if(numero>=-32768&&numero<=32767){
                return true;
            }
        }
        return false;
    }
    public boolean esEntero(String dato){
        boolean isNumeric =  dato.matches("[+-]?\\d*?");
        
        if(isNumeric){ // ya tenemos numeros, validar rango
            int numero=Integer.parseInt(dato);
            if(numero>=-2147483647&&numero<=2147483647){
                return true;
            }
        }
        return false;
    }
    public boolean esLong(String dato){
        boolean isLong =  dato.matches("[+-]?\\d*?");
        long inicio=-9223372036854775808L;
        long fin=9223372036854775807L;
        if (isLong) { // ya tenemos numeros, validar rango
            Integer numero = Integer.parseInt(dato);
            if (numero >= inicio  && numero <= fin) {
                return true;
            }
        }
        return false;
    }

    public boolean esFloat(String dato) {
        boolean isDouble = dato.matches("[+-]?\\d*\\.\\d+");
        if (isDouble) {
            if (dato.length() <= 10) {
                return true;
            }
        }
        return false;
    }

    public boolean esDouble(String dato) {
        boolean isDouble = dato.matches("[+-]?\\d*\\.\\d+");
        return isDouble;
    }

